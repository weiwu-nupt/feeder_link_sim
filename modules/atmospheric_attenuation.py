#  结果 dataclass
# ══════════════════════════════════════════════════════════

@dataclass
class AtmAttenuationResult:
    freq_ghz:         float
    elevation_deg:    float
    pressure_hpa:     float
    temperature_k:    float
    water_vapor_hpa:  float

    gamma_o2:   float   # O₂ 比衰减 dB/km
    gamma_h2o:  float   # H₂O 比衰减 dB/km
    gamma_total:float   # 总比衰减 dB/km

    A_o2:       float   # O₂ 斜路径衰减 dB
    A_h2o:      float   # H₂O 斜路径衰减 dB
    A_total:    float   # 总斜路径衰减 dB


from dataclasses import dataclass   # 确保导入


def compute_atm_attenuation(
    freq_ghz:          float,
    elevation_deg:     float  = 10.0,
    pressure_hpa:      float  = 1013.25,
    temperature_k:     float  = 288.15,
    water_vapor_hpa:   float  = 10.0,
    station_alt_km:    float  = 0.0,
) -> AtmAttenuationResult:
    """
    计算斜路径大气气体衰减（O₂ + H₂O）。

    大气参数默认值（ITU-R P.835 标准大气，海平面）：
      pressure    = 1013.25 hPa
      temperature = 288.15 K  (15°C)
      water_vapor = 10.0 hPa  (≈ 60% RH @ 15°C)
    """
    g_o2, g_h2o, g_tot, A_o2, A_h2o = calc_path_attenuation_gas(
        freq_ghz, elevation_deg, pressure_hpa,
        temperature_k, water_vapor_hpa, station_alt_km)

    return AtmAttenuationResult(
        freq_ghz        = freq_ghz,
        elevation_deg   = elevation_deg,
        pressure_hpa    = pressure_hpa,
        temperature_k   = temperature_k,
        water_vapor_hpa = water_vapor_hpa,
        gamma_o2        = g_o2,
        gamma_h2o       = g_h2o,
        gamma_total     = g_tot,
        A_o2            = A_o2,
        A_h2o           = A_h2o,
        A_total         = A_o2 + A_h2o,
    )