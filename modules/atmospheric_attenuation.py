# ══════════════════════════════════════════════════════════
#  单频点比衰减计算
# ══════════════════════════════════════════════════════════

def _o2_line_contribution(f, f0, a1, a2, a3, a4, a5, a6, p, e, theta):
    """O₂ 单条谱线的复折射率虚部贡献（P.676-13 式 3-7）"""
    S_i     = a1 * 1e-7 * p * theta**3 * math.exp(a2 * (1 - theta))
    Df      = a3 * 1e-4 * (p * theta**(0.8 - a4) + 1.1 * e * theta)
    Df      = math.sqrt(Df**2 + 2.25e-6)                      # 塞曼展宽
    delta   = (a5 + a6 * theta) * 1e-4 * (p + e) * theta**0.8
    t1      = (Df - delta * (f0 - f)) / ((f0 - f)**2 + Df**2)
    t2      = (Df - delta * (f0 + f)) / ((f0 + f)**2 + Df**2)
    F_i     = (f / f0) * (t1 + t2)
    return S_i * F_i


def _h2o_line_contribution(f, f0, b1, b2, b3, b4, b5, b6, p, e, theta):
    """H₂O 单条谱线的复折射率虚部贡献（P.676-13 式 3-7，H₂O 专属参数）"""
    if e <= 0:
        return 0.0
    S_i  = b1 * 1e-1 * e * theta**3.5 * math.exp(b2 * (1 - theta))
    Df   = b3 * 1e-4 * (p * theta**b4 + b5 * e * theta**b6)
    Df   = 0.535 * Df + math.sqrt(0.217 * Df**2 +
                                   2.1316e-12 * f0**2 / theta)
    t1   = Df / ((f0 - f)**2 + Df**2)
    t2   = Df / ((f0 + f)**2 + Df**2)
    F_i  = (f / f0) * (t1 + t2)
    return S_i * F_i


def _dry_continuum(f, p, e, theta):
    """干空气连续谱（Debye + N₂，P.676-13 式 8）"""
    d    = 5.6e-4 * (p + e) * theta**0.8
    t1   = 6.14e-5 / (d * (1 + (f / d)**2))
    t2   = 1.4e-12 * p * theta**1.5 / (1 + 1.9e-5 * f**1.5)
    return f * p * theta**2 * (t1 + t2)


def calc_specific_attenuation_gas(
    freq_ghz: float,
    pressure_hpa: float  = 1013.25,
    temperature_k: float = 288.15,
    water_vapor_hpa: float = 10.0,
) -> tuple[float, float, float]:
    """
    单频点气体比衰减（P.676-13 逐线模型）。

    参数
    ----
    freq_ghz        : 频率 (GHz)
    pressure_hpa    : 干空气气压 (hPa)，标准海平面 1013.25
    temperature_k   : 温度 (K)，标准 288.15 (15°C)
    water_vapor_hpa : 水汽分压 e (hPa)
                      e = ρ·R·T/Mw，实用近似：
                      相对湿度 RH(%) → e ≈ RH/100 × 6.112 × exp(17.67(T-273)/(T-29.5))
                      默认 10 hPa ≈ 60% RH @ 15°C

    返回
    ----
    (gamma_o2, gamma_h2o, gamma_total)  单位 dB/km
    """
    f     = freq_ghz
    p     = pressure_hpa
    e     = water_vapor_hpa
    T     = temperature_k
    theta = 300.0 / T

    # O₂ 谱线求和
    N_o2 = sum(
        _o2_line_contribution(f, *row, p, e, theta)
        for row in _O2_LINES
    )
    N_o2 += _dry_continuum(f, p, e, theta)
    gamma_o2 = 0.1820 * f * N_o2

    # H₂O 谱线求和
    N_h2o = sum(
        _h2o_line_contribution(f, *row, p, e, theta)
        for row in _H2O_LINES
    )
    gamma_h2o = 0.1820 * f * N_h2o

    gamma_total = gamma_o2 + gamma_h2o
    return float(gamma_o2), float(gamma_h2o), float(gamma_total)


# ══════════════════════════════════════════════════════════
#  斜路径大气衰减（P.676-13 Annex 2 简化等效高度法）
# ══════════════════════════════════════════════════════════

# O₂ 等效均匀大气高度：约 6 km（标准大气，对流层内）
# H₂O 等效均匀大气高度：约 2 km（标准大气，对流层内）
# 这两个值来自 P.676-13 Annex 2 的参考数值，
# 严格计算应对标准大气廓线做逐层积分，此处取典型值。
_H_O2   = 6.0   # km
_H_H2O  = 2.0   # km


def calc_path_attenuation_gas(
    freq_ghz: float,
    elevation_deg: float,
    pressure_hpa: float   = 1013.25,
    temperature_k: float  = 288.15,
    water_vapor_hpa: float= 10.0,
    station_alt_km: float = 0.0,
) -> tuple[float, float, float, float, float]:
    """
    斜路径气体衰减 (dB)，P.676-13 等效高度法。

    斜路径积分：
      对于 θ ≥ 5°：A = γ · H_equiv / sin(θ)
      对于 θ < 5°：使用余弦定理修正（与 P.618 雨衰相同的低仰角修正）

    O₂ 和 H₂O 使用各自的等效高度分别积分，不能混用雨衰路径。

    返回
    ----
    (gamma_o2, gamma_h2o, gamma_total,
     A_o2_dB, A_h2o_dB, A_total_dB)
    实际返回 5 个值：gamma_o2, gamma_h2o, gamma_total, A_o2, A_h2o
    从中可得 A_total = A_o2 + A_h2o
    """
    gamma_o2, gamma_h2o, gamma_total = calc_specific_attenuation_gas(
        freq_ghz, pressure_hpa, temperature_k, water_vapor_hpa)

    theta     = math.radians(elevation_deg)
    sin_theta = math.sin(theta)

    def _slant(gamma, H_equiv):
        """等效高度法斜路径积分"""
        h_eff = max(H_equiv - station_alt_km, 0.0)
        if elevation_deg >= 5.0:
            return gamma * h_eff / sin_theta
        else:
            # 低仰角余弦定理修正（Re = 8500 km）
            Re = 8500.0
            L  = (2 * h_eff) / (
                math.sqrt(sin_theta**2 + 2 * h_eff / Re) + sin_theta)
            return gamma * L

    A_o2  = _slant(gamma_o2,  _H_O2)
    A_h2o = _slant(gamma_h2o, _H_H2O)

    return gamma_o2, gamma_h2o, gamma_total, A_o2, A_h2o


